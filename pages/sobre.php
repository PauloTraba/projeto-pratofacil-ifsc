  <h1>Sobre o Projeto Prato Fácil</h1>
  <p>
   O projeto <strong>Prato Fácil</strong> foi desenvolvido com o objetivo de criar uma plataforma acessível e intuitiva que
   permite aos usuários encontrar receitas de forma fácil e rápida, utilizando os ingredientes que já possuem em casa.
   Além disso, o sistema oferece funcionalidades adicionais como a criação de listas de compras, salvamento de receitas favoritas
   e avaliações com estrelas, todas acessíveis aos usuários que se cadastrarem e fizerem login no sistema.
  </p>

  <h2>Intenção do Projeto</h2>
  <p>
   A intenção deste projeto é fornecer uma ferramenta útil para cozinheiros amadores e profissionais, permitindo
   a descoberta de novas receitas com base nos ingredientes disponíveis. Através de uma interface amigável e
   intuitiva, o <strong>Prato Fácil</strong> busca facilitar o processo de planejamento e execução de refeições,
   promovendo uma experiência culinária eficiente e prazerosa.
  </p>

  <h2>Proposta</h2>
  <p>
   O <strong>Prato Fácil</strong> propõe-se a ser uma solução completa para gestão de receitas e ingredientes,
   permitindo que os usuários busquem receitas, salvem suas favoritas, criem listas de compras personalizadas
   e avaliem receitas. O sistema é projetado para ser acessível a todos, com uma interface simples que permite
   a navegação fácil, seja no desktop ou em dispositivos móveis.
  </p>

  <h2>Tecnologias Usadas</h2>
  <p>
   O projeto foi desenvolvido utilizando as seguintes tecnologias:
  <ul>
   <li><strong>HTML5</strong> e <strong>CSS3</strong> para estrutura e estilização das páginas.</li>
   <li><strong>PHP</strong> para a criação da lógica do lado do servidor e interação com o banco de dados.</li>
   <li><strong>MySQL</strong> como sistema de gerenciamento de banco de dados para armazenar informações de usuários, receitas e avaliações.</li>
   <li><strong>JavaScript</strong> para a criação de interações dinâmicas e responsivas.</li>
  </ul>
  </p>

